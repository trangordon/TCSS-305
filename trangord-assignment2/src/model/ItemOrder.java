package model;

/**
 * The Class ItemOrder.
 * @author Gordon Tran
 * @version October 2018
 */
public final class ItemOrder {


    /** The quantity. */
    private int myQuant;
    
    /** The item. */
    private Item myItem;


    /**
     * Instantiates a new item order.
     *
     * @param theItem the the item
     * @param theQuantity the the quantity
     */
    public ItemOrder(final Item theItem, final int theQuantity) {
        myItem = theItem;
        myQuant = theQuantity;
    }


    /**
     * Gets the item.
     *
     * @return the item
     */
    public Item getItem() {
        
        return myItem;
    }
    
    /**
     * Gets the quantity.
     *
     * @return the quantity
     */
    public int getQuantity() {
        
        return myQuant;
    }

    @Override
    public String toString() {
        
        return myQuant + " of " + myItem;
    }

}
